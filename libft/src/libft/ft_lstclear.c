#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*node;
	t_list	*temp;

	node = *lst;
	while (node)
	{
		del(node->content);
		temp = node;
		node = node->next;
		free(temp);
	}
	*lst = NULL;
}
